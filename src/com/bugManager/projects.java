package com.bugManager;

public class projects {
    public String projectId;

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String projectTitle;
    public String projectDetails;
    public String projectLeaderId;
    public String projectHeadUsername;

    public String getProjectHeadUsername() {
        return projectHeadUsername;
    }

    public void setProjectHeadUsername(String projectHeadUsername) {
        this.projectHeadUsername = projectHeadUsername;
    }

    public String getProjectTitle() {
        return projectTitle;
    }

    public void setProjectTitle(String projectTitle) {
        this.projectTitle = projectTitle;
    }

    public String getProjectDetails() {
        return projectDetails;
    }

    public void setProjectDetails(String projectDetails) {
        this.projectDetails = projectDetails;
    }

    public String getProjectLeaderId() {
        return projectLeaderId;
    }

    public void setProjectLeaderId(String projectLeaderId) {
        this.projectLeaderId = projectLeaderId;
    }
}
