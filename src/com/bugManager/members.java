package com.bugManager;

public class members {
    public String membersId, membersFirstName, membersLastName, membersUsername;

    public String getMembersId() {
        return membersId;
    }

    public void setMembersId(String membersId) {
        this.membersId = membersId;
    }

    public String getMembersFirstName() {
        return membersFirstName;
    }

    public void setMembersFirstName(String membersFirstName) {
        this.membersFirstName = membersFirstName;
    }

    public String getMembersLastName() {
        return membersLastName;
    }

    public void setMembersLastName(String membersLastName) {
        this.membersLastName = membersLastName;
    }

    public String getMembersUsername() {
        return membersUsername;
    }

    public void setMembersUsername(String membersUsername) {
        this.membersUsername = membersUsername;
    }
}
